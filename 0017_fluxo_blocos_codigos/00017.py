# if | elif       | else
# se | se não se  | se não

val1 = False
val2 = False
val3 = True
val4 = True
val5 = False

if val1:
    print(f'{val1=}')
elif val2:
    print(f'{val2=}')
elif val3:
    print(f'{val3=}')
elif val4:
    print(f'{val4=}')
elif val5:
    print(f'{val5=}')
else:
    print('Nenhuma condição foi atendida.')